        'ip'          => $_GET['ip']   ?? null,
    ], $r);

    $w = IST_W;

    $out = ist_term_header($r);

    /* Connection Results */
    $out .= ist_sec('Connection Results');

    $dl   = isset($r['download'])    ? (float)$r['download']    : null;
    $ul   = isset($r['upload'])      ? (float)$r['upload']      : null;
    $ping = isset($r['ping'])        ? (float)$r['ping']        : null;
    $jit  = isset($r['jitter'])      ? (float)$r['jitter']      : null;
    $loss = isset($r['packet_loss']) ? (float)$r['packet_loss'] : null;

    if ($dl !== null) {
        $dlClr = $dl >= 100 ? IST_ABGR : ($dl >= 25 ? IST_ABYE : IST_ABRE);
        $dlBar = ist_bar($dl, 1000);
        $out  .= ist_row('Download Speed', $dlClr . number_format($dl, 1) . ' Mbps' . IST_A0 . '  ' . $dlClr . $dlBar . IST_A0);
    }
    if ($ul !== null) {
        $ulClr = $ul >= 50 ? IST_ABGR : ($ul >= 10 ? IST_ABYE : IST_ABRE);
        $ulBar = ist_bar($ul, 500);
        $out  .= ist_row('Upload Speed',   $ulClr . number_format($ul, 1) . ' Mbps' . IST_A0 . '  ' . $ulClr . $ulBar . IST_A0);
    }
    if ($ping !== null) {
        $pingClr = $ping <= 20 ? IST_ABGR : ($ping <= 60 ? IST_ABYE : IST_ABRE);
        $out    .= ist_row('Latency (Ping)', $pingClr . number_format($ping, 1) . ' ms' . IST_A0);
    }
    if ($jit !== null) {
        $jitClr = $jit <= 5 ? IST_ABGR : ($jit <= 20 ? IST_ABYE : IST_ABRE);
        $out   .= ist_row('Jitter',         $jitClr . number_format($jit, 1) . ' ms' . IST_A0);
    }
    if ($loss !== null) {
        $lossClr = $loss <= 1 ? IST_ABGR : ($loss <= 5 ? IST_ABYE : IST_ABRE);
        $out    .= ist_row('Packet Loss',   $lossClr . number_format($loss, 1) . '%' . IST_A0);
    }

    /* Timing Metrics */
    if (!empty($r['dns_ms']) || !empty($r['ttfb_ms']) || !empty($r['tls_ms'])) {
        $out .= ist_sec('Timing Metrics');
        if (!empty($r['dns_ms']))   $out .= ist_row('DNS Resolution',  number_format((float)$r['dns_ms'], 1)  . ' ms');
        if (!empty($r['tls_ms']))   $out .= ist_row('TLS Handshake',   number_format((float)$r['tls_ms'], 1)  . ' ms');
        if (!empty($r['ttfb_ms']))  $out .= ist_row('Time to 1st Byte',number_format((float)$r['ttfb_ms'], 1) . ' ms');
        if (!empty($r['http_ms']))  $out .= ist_row('HTTP Response',   number_format((float)$r['http_ms'], 1)  . ' ms');
    }

    /* Quality Scores */
    if (!empty($r['scores']) && is_array($r['scores'])) {
        $out .= ist_sec('Quality Scores');
        foreach ($r['scores'] as $name => $score) {
            $sc    = (int)$score;
            $grade = $sc >= 90 ? 'A+' : ($sc >= 80 ? 'A' : ($sc >= 65 ? 'B' : ($sc >= 50 ? 'C' : ($sc >= 35 ? 'D' : 'F'))));
            $clr   = ist_grade_clr($grade);
            $bar   = ist_bar($sc, 100, 18);
            $out  .= ist_row($name, $clr . IST_AB . str_pad($sc, 3) . '/100' . IST_A0 . '  ' . $clr . $bar . IST_A0 . '  ' . $clr . IST_AB . "Grade {$grade}" . IST_A0);
        }
    }

    /* Overall Grade */
    if (!empty($r['grade']) || !empty($r['score'])) {
        $out  .= ist_sec('Overall Assessment');
        $grade = $r['grade'] ?? '—';
        $score = isset($r['score']) ? (int)$r['score'] : null;
        $clr   = ist_grade_clr($grade);
        if ($score !== null) $out .= ist_row('Network Score', $clr . IST_AB . "{$score}/100" . IST_A0);
        $out .= ist_row('Connection Grade', $clr . IST_AB . $grade . IST_A0);
    }

    /* Environment */
    if (!empty($r['ip']) || !empty($r['user_agent']) || !empty($r['connection_type'])) {
        $out .= ist_sec('Environment');
        if (!empty($r['ip']))              $out .= ist_row('Client IP',       $r['ip']);
        if (!empty($r['connection_type'])) $out .= ist_row('Connection Type', $r['connection_type']);
        if (!empty($r['effective_type']))  $out .= ist_row('Effective Type',  $r['effective_type']);
        if (!empty($r['downlink']))        $out .= ist_row('Est. Downlink',   $r['downlink'] . ' Mbps');
        if (!empty($r['browser']))         $out .= ist_row('Browser',         $r['browser']);
        if (!empty($r['platform']))        $out .= ist_row('Platform',        $r['platform']);
    }

    /* Recommendations */
    $recs = $r['recommendations'] ?? [];
    if (empty($recs) && ($ping !== null || $dl !== null)) {
        // Auto-generate basic recommendations
        if ($ping !== null && $ping > 60)  $recs[] = 'High latency detected. Check for background processes using the network or consider a wired connection.';
        if ($jit  !== null && $jit  > 20)  $recs[] = 'Significant jitter observed. This may impact real-time applications like video calls and gaming.';
        if ($loss !== null && $loss > 2)   $recs[] = 'Packet loss above 2% detected. Check cables, router firmware, and contact your ISP if persistent.';
        if ($dl   !== null && $dl   < 25)  $recs[] = 'Download speed below 25 Mbps. Consider upgrading your plan or checking for network congestion.';
        if ($ul   !== null && $ul   < 5)   $recs[] = 'Upload speed is low. This may impact video calls, cloud uploads, and remote desktop performance.';
        if (empty($recs))                  $recs[] = 'Connection is performing well across all measured parameters.';
    }
    if (!empty($recs)) {
        $out .= ist_sec('Recommendations');
        foreach ($recs as $i => $rec) {
            $num  = IST_ABYE . IST_AB . ($i + 1) . '.' . IST_A0;
            $out .= "  {$num}  " . IST_AWH . wordwrap($rec, 58, "\n     ", true) . IST_A0 . "\n";
        }
    }

    $out .= ist_term_footer();
    echo $out;
    exit;
}

