    /* ── Helper: extract µs-accurate timing from curl_getinfo ───────────── */
    function ist_la_timing(array $i): array {
        $us = isset($i['namelookup_time_us']); // PHP 7.3+ / curl 7.61+
        $ms = fn(string $k) => $us ? ($i[$k.'_us'] ?? 0) / 1000.0 : ($i[$k] ?? 0.0) * 1000.0;

        $dns   = $ms('namelookup_time');
        $conn  = $ms('connect_time');
        $app   = $ms('appconnect_time');   // 0 for plain HTTP
        $pre   = $ms('pretransfer_time');
        $stx   = $ms('starttransfer_time');
        $total = $ms('total_time');

        return [
            'dns_ms'      => round($dns,                         3),
            'tcp_ms'      => round(max(0, $conn - $dns),         3),
            'tls_ms'      => round($app > 0 ? max(0,$app-$conn) : 0.0, 3),
            'ttfb_ms'     => round(max(0, $stx - $pre),         3),
            'transfer_ms' => round(max(0, $total - $stx),       3),
            'total_ms'    => round($total,                       3),
            'http_code'   => (int)($i['http_code'] ?? 0),
        ];
    }

