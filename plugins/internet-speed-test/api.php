/* ── download (legacy single-chunk) ────────────────────────────────────── */
if ($action === 'download') {
    $size = min((int)($_GET['size'] ?? 1048576), 8388608); // max 8MB per chunk
    header('Content-Type: application/octet-stream');
    header('Content-Length: ' . $size);
    header('Cache-Control: no-store');
    header('X-Content-Size: ' . $size);

    $chunk  = 65536;
    $sent   = 0;
    $rndBuf = random_bytes(min($chunk, $size));
    while ($sent < $size) {
        $toSend = min($chunk, $size - $sent);
        echo substr($rndBuf, 0, $toSend);
        $sent += $toSend;
        if (connection_aborted()) break;
    }
    exit;
}

/* ── download_stream (parallel streaming — high accuracy) ───────────────── */
if ($action === 'download_stream') {
    $duration = (float)min((float)($_GET['duration'] ?? 10), 30);

    header('Content-Type: application/octet-stream');
    header('Content-Encoding: identity');   // prevent any gzip/deflate that hides true byte count
    header('Cache-Control: no-store, no-cache');
    header('X-Accel-Buffering: no');
    header('X-Content-Type-Options: nosniff');

    while (ob_get_level() > 0) { @ob_end_clean(); }

    // XOR-based buffer: each write is unique, prevents any transparent compression
    $seed  = random_bytes(65536);
    $mask  = random_bytes(65536);
    $xored = $seed ^ $mask;
    $buf   = str_repeat($xored, 64);   // 4 MB per write

    $endTime = microtime(true) + $duration;
    $iter    = 0;

    while (microtime(true) < $endTime) {
        echo $buf;
        $iter++;
        if ($iter % 4 === 0) {
            @ob_flush();
            @flush();
            if (connection_aborted()) break;
        }
    }
    exit;
}

/* ── upload ─────────────────────────────────────────────────────────────── */
if ($action === 'upload') {
    header('Content-Type: application/json; charset=utf-8');
    // Always count actual bytes received via fread — never trust CONTENT_LENGTH
    // (proxies and built-in server can report it before the body is fully read).
    $bytes = 0;
    $input = fopen('php://input', 'rb');
    while (!feof($input)) {
        $chunk  = fread($input, 131072);
        $bytes += strlen($chunk);
    }
    fclose($input);
    echo json_encode(['ok' => true, 'received' => $bytes, 'ts' => microtime(true)]);
    exit;
}

/* ── myip ───────────────────────────────────────────────────────────────── */
if ($action === 'myip') {
    header('Content-Type: application/json; charset=utf-8');

    // Detect real IP from headers (shared hosting / proxy aware)
    $ip = '';
    foreach (['HTTP_CF_CONNECTING_IP', 'HTTP_X_REAL_IP', 'HTTP_X_FORWARDED_FOR', 'REMOTE_ADDR'] as $k) {
        if (!empty($_SERVER[$k])) {
            $candidate = trim(explode(',', $_SERVER[$k])[0]);
            if (filter_var($candidate, FILTER_VALIDATE_IP)) { $ip = $candidate; break; }
        }
    }

    $isIPv6    = filter_var($ip, FILTER_VALIDATE_IP, FILTER_FLAG_IPV6) !== false;
    $isPrivate = filter_var($ip, FILTER_VALIDATE_IP, FILTER_FLAG_NO_PRIV_RANGE | FILTER_FLAG_NO_RES_RANGE) === false;

    // Server environment
    $phpVer   = PHP_VERSION;
    $sapi     = php_sapi_name();
    $server   = $_SERVER['SERVER_SOFTWARE'] ?? 'Unknown';
    $hostname = gethostname() ?: 'Unknown';
    $serverIp = gethostbyname($hostname);

    // Check IPv6 support server-side
    $ipv6Support = defined('AF_INET6');

    // ISP / geo lookup via ip-api.com (free, no key required)
    $isp = [];
    if ($ip && !$isPrivate) {
        $ctx = stream_context_create(['http' => ['timeout' => 3, 'ignore_errors' => true]]);
        $raw = @file_get_contents(
            'http://ip-api.com/json/' . urlencode($ip) . '?fields=status,country,countryCode,regionName,city,isp,org,as',
            false, $ctx
        );
        if ($raw) {
            $parsed = @json_decode($raw, true);
            if (!empty($parsed) && ($parsed['status'] ?? '') === 'success') {
                $isp = [
                    'isp'          => $parsed['isp']        ?? '',
                    'org'          => $parsed['org']         ?? '',
                    'as'           => $parsed['as']          ?? '',
                    'country'      => $parsed['country']     ?? '',
                    'country_code' => $parsed['countryCode'] ?? '',
                    'region'       => $parsed['regionName']  ?? '',
                    'city'         => $parsed['city']        ?? '',
                ];
            }
        }
    }

    echo json_encode([
        'ip'           => $ip,
        'is_ipv6'      => $isIPv6,
        'is_private'   => $isPrivate,
        'ipv6_support' => $ipv6Support,
        'server'       => $server,
        'php'          => $phpVer,
        'hostname'     => $hostname,
        'server_ip'    => $serverIp !== $hostname ? $serverIp : null,
        'timestamp'    => microtime(true),
        'date'         => date('c'),
        'isp'          => $isp,
    ]);
    exit;
}

