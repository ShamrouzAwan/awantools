    ];

    // Memory
    $result['memory_peak_mb'] = round(memory_get_peak_usage(true) / 1048576, 2);

    // TCP socket probes
    foreach ([80 => 'tcp_port80_ms', 443 => 'tcp_port443_ms'] as $port => $key) {
        $t0  = microtime(true);
        $sock = @fsockopen('8.8.8.8', $port, $errno, $errstr, 1);
        if ($sock) {
            $result[$key] = round((microtime(true) - $t0) * 1000, 1);
            fclose($sock);
        } else {
            $result[$key] = null;
        }
    }

    // Execution time (measured last)
    $result['exec_time_ms'] = round((microtime(true) - $tStart) * 1000, 2);

    echo json_encode($result);
    exit;
}

