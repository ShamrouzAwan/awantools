    /* ── Statistics ─────────────────────────────────────────────────────── */
    function ist_la_stats(array $vals): array {
        if (!$vals) return array_fill_keys(['min','max','avg','median','stddev','jitter','p95'], 0.0);
        sort($vals);
        $n      = count($vals);
        $min    = $vals[0]; $max = $vals[$n-1];
        $avg    = array_sum($vals) / $n;
        $mid    = (int)($n / 2);
        $median = ($n%2===0) ? ($vals[$mid-1]+$vals[$mid])/2.0 : $vals[$mid];
        $stddev = sqrt(array_sum(array_map(fn($v)=>($v-$avg)**2, $vals))/$n);
        $diffs  = [];
        for ($j=1;$j<$n;$j++) $diffs[]=abs($vals[$j]-$vals[$j-1]);
        $jitter = $diffs ? array_sum($diffs)/count($diffs) : 0.0;
        $p95    = $vals[max(0,min((int)ceil(0.95*$n)-1,$n-1))];
        return [
            'min'=>round($min,3),'max'=>round($max,3),'avg'=>round($avg,3),
            'median'=>round($median,3),'stddev'=>round($stddev,3),
            'jitter'=>round($jitter,3),'p95'=>round($p95,3),
        ];
    }

    /* ── Outlier removal (IQR method) ───────────────────────────────────── */
    function ist_la_deoutlier(array $probes, string $key): array {
        if (count($probes) <= 4) return ['filtered'=>$probes,'removed'=>0];
        $vals = array_column($probes, $key); sort($vals); $n = count($vals);
        $q1 = $vals[(int)floor(($n-1)*0.25)];
        $q3 = $vals[(int)floor(($n-1)*0.75)];
        $iqr = $q3 - $q1;
        $lo  = $q1 - 1.5*$iqr;  $hi = $q3 + 1.5*$iqr;
        $out = array_values(array_filter($probes, fn($p) => isset($p[$key]) && $p[$key]>=$lo && $p[$key]<=$hi));
        return ['filtered'=>$out,'removed'=>count($probes)-count($out)];
    }

    /* ── Per-segment stats helper ───────────────────────────────────────── */
    function ist_la_segstats(array $probes, string $key): array {
        $vals = array_values(array_filter(array_column($probes, $key), fn($v)=>is_numeric($v)));
        if (!$vals) return ['avg'=>null,'min'=>null,'max'=>null,'samples'=>0];
        $s = ist_la_stats($vals); $s['samples'] = count($vals); return $s;
    }

    /* ── Color-coded rating ─────────────────────────────────────────────── */
    function ist_la_rate(float $ms, array $t): array {
        [$exc,$good,$fair] = $t;
        if ($ms<=$exc)  return ['label'=>'Excellent','cls'=>'excellent','score'=>100];
        if ($ms<=$good) return ['label'=>'Good','cls'=>'good','score'=>(int)(75+25*(1-($ms-$exc)/max(1,$good-$exc)))];
        if ($ms<=$fair) return ['label'=>'Fair','cls'=>'fair','score'=>(int)(50+25*(1-($ms-$good)/max(1,$fair-$good)))];
        return ['label'=>'Poor','cls'=>'poor','score'=>max(0,(int)(50*(1-min(1,($ms-$fair)/max(1,$fair)))))];
    }

    /* ── SQLite history (stored in sys temp dir) ────────────────────────── */
    function ist_la_db(): ?PDO {
        try {
            $f   = sys_get_temp_dir() . '/ist_latency_v2.db';
            $pdo = new PDO('sqlite:'.$f);
            $pdo->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);
            $pdo->exec('CREATE TABLE IF NOT EXISTS history (
                id INTEGER PRIMARY KEY AUTOINCREMENT,
                url TEXT NOT NULL, tested_at INTEGER NOT NULL,
                count INTEGER, method TEXT,
                avg_ms REAL, min_ms REAL, max_ms REAL, median_ms REAL,
                stddev_ms REAL, jitter_ms REAL, p95_ms REAL, packet_loss REAL,
                dns_ms REAL, tcp_ms REAL, tls_ms REAL, ttfb_ms REAL,
                ip TEXT, http_code INTEGER, http_version TEXT, rating TEXT
            )');
            return $pdo;
        } catch (Exception $e) { return null; }
    }

    function ist_la_save(string $url, array $r): int {
        $pdo = ist_la_db(); if (!$pdo) return 0;
        try {
            $s = $r['stats']; $sg = $r['segments']; $m = $r['meta'] ?? [];
            $pdo->prepare(
                'INSERT INTO history (url,tested_at,count,method,avg_ms,min_ms,max_ms,median_ms,
                 stddev_ms,jitter_ms,p95_ms,packet_loss,dns_ms,tcp_ms,tls_ms,ttfb_ms,
                 ip,http_code,http_version,rating) VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)'
            )->execute([
                $url, time(), $r['count'], $r['method'],
                $s['avg'], $s['min'], $s['max'], $s['median'],
                $s['stddev'], $s['jitter'], $s['p95'], $s['packet_loss'],
                $sg['dns']['avg'] ?? null, $sg['tcp']['avg'] ?? null,
                $sg['tls']['avg'] ?? null, $sg['ttfb']['avg'] ?? null,
                $m['ip'] ?? null, $m['http_code'] ?? null, $m['http_version'] ?? null,
                $r['ratings']['overall']['label'] ?? null,
            ]);
            return (int)$pdo->lastInsertId();
        } catch (Exception $e) { return 0; }
    }

    function ist_la_history(string $url, int $limit = 10): array {
        $pdo = ist_la_db(); if (!$pdo) return [];
        try {
            $st = $pdo->prepare(
                'SELECT id,tested_at,avg_ms,min_ms,max_ms,median_ms,jitter_ms,p95_ms,
                        packet_loss,dns_ms,ttfb_ms,http_code,rating
                   FROM history WHERE url=? ORDER BY tested_at DESC LIMIT ?'
            );
            $st->execute([$url, $limit]);
            return $st->fetchAll(PDO::FETCH_ASSOC);
        } catch (Exception $e) { return []; }
    }

    /* ═══════════════════════════════════════════════════════════════════════
     * MEASUREMENT LOOP
     * ═══════════════════════════════════════════════════════════════════════ */
    $wallStart = function_exists('hrtime') ? hrtime(true) : (int)(microtime(true)*1e9);
    $probes    = [];
    $meta      = null;
    $failed    = 0;

    /* ── Probe 1: fresh, serial — collects headers + metadata ───────────── */
    {
        $ch  = ist_la_build($rawUrl, $method, $connTout, $reqTout, true, $resolveEntry, $followR);
        $raw = curl_exec($ch);
        $inf = curl_getinfo($ch);
        $err = curl_errno($ch);
        $em  = $err ? curl_error($ch) : null;
        curl_close($ch);

        if (!$err && $inf['http_code'] > 0) {
            $t = ist_la_timing($inf);
            $t['n'] = 1; $t['ok'] = true; $t['is_fresh'] = true; $t['error'] = null;
            $probes[] = $t;
            $meta = ist_la_meta($inf, is_string($raw) ? $raw : '');
        } else {
            $failed++;
            $probes[] = ['n'=>1,'ok'=>false,'is_fresh'=>true,'error'=>$em];
        }
    }

    /* ── Probes 2-N: parallel via curl_multi in batches of 10 ───────────── */
    if ($count > 1) {
        if (function_exists('curl_multi_init')) {
            $remaining = $count - 1;
            $pIdx      = 2;
            $batchSize = min(10, $remaining);

            while ($remaining > 0) {
                $batch   = min($batchSize, $remaining);
                $mh      = curl_multi_init();
                $handles = [];
                for ($b = 0; $b < $batch; $b++) {
                    $ch = ist_la_build($rawUrl, $method, $connTout, $reqTout, false, $resolveEntry, $followR);
                    $handles[] = ['ch'=>$ch, 'n'=>$pIdx++];
                    curl_multi_add_handle($mh, $ch);
                }
                $running = null;
                do {
                    curl_multi_exec($mh, $running);
                    if ($running > 0) curl_multi_select($mh, 0.01);
                } while ($running > 0);

                foreach ($handles as $entry) {
                    $ch  = $entry['ch'];
                    $inf = curl_getinfo($ch);
                    $err = curl_errno($ch);
                    $em  = $err ? curl_error($ch) : null;
                    if (!$err && $inf['http_code'] > 0) {
                        $t = ist_la_timing($inf);
                        $t['n']=$entry['n']; $t['ok']=true; $t['is_fresh']=false; $t['error']=null;
                        $probes[] = $t;
                        if (!$meta) {
                            $raw  = curl_multi_getcontent($ch);
                            $meta = ist_la_meta($inf, is_string($raw)?$raw:'');
                        }
                    } else {
                        $failed++;
                        $probes[] = ['n'=>$entry['n'],'ok'=>false,'is_fresh'=>false,'error'=>$em];
                    }
                    curl_multi_remove_handle($mh, $ch);
                    curl_close($ch);
                }
                curl_multi_close($mh);
                $remaining -= $batch;
            }
        } else {
            /* Fallback: sequential probes when curl_multi unavailable */
            for ($i = 2; $i <= $count; $i++) {
                $ch  = ist_la_build($rawUrl, $method, $connTout, $reqTout, false, $resolveEntry, $followR);
                $raw = curl_exec($ch);
                $inf = curl_getinfo($ch);
                $err = curl_errno($ch);
                $em  = $err ? curl_error($ch) : null;
                curl_close($ch);
                if (!$err && $inf['http_code']>0) {
                    $t=ist_la_timing($inf); $t['n']=$i; $t['ok']=true; $t['is_fresh']=false; $t['error']=null;
                    $probes[]=$t;
                } else {
                    $failed++;
                    $probes[]=['n'=>$i,'ok'=>false,'is_fresh'=>false,'error'=>$em];
                }
            }
        }
    }

    /* ── Post-processing ────────────────────────────────────────────────── */
    $succeeded   = array_values(array_filter($probes, fn($p)=>$p['ok']));
    $packetLoss  = $count>0 ? round(($failed/$count)*100,1) : 100.0;
    $outlierInfo = ist_la_deoutlier($succeeded, 'total_ms');
    $clean       = $outlierInfo['filtered'];
    $outRmvd     = $outlierInfo['removed'];

    /* Per-segment statistics (on outlier-cleaned probes) */
    $segments = [
        'dns'      => ist_la_segstats($clean, 'dns_ms'),
        'tcp'      => ist_la_segstats($clean, 'tcp_ms'),
        'tls'      => ist_la_segstats($clean, 'tls_ms'),
        'ttfb'     => ist_la_segstats($clean, 'ttfb_ms'),
        'transfer' => ist_la_segstats($clean, 'transfer_ms'),
        'total'    => ist_la_segstats($clean, 'total_ms'),
    ];

    /* Overall latency statistics */
    $totalVals = array_column($clean, 'total_ms');
    $stats     = ist_la_stats($totalVals);
    $stats['packet_loss']      = $packetLoss;
    $stats['samples']          = $count;
    $stats['succeeded']        = count($succeeded);
    $stats['failed']           = $failed;
    $stats['outliers_removed'] = $outRmvd;
    $stats['dns_fresh_ms']     = $freshDnsMs;

    /* Ratings */
    $ratings = [
        'overall' => ist_la_rate($stats['avg']               ?? 999, [50,  100, 200]),
        'ttfb'    => ist_la_rate($segments['ttfb']['avg']    ?? 999, [20,   50, 100]),
        'dns'     => ist_la_rate($segments['dns']['avg']     ?? 999, [10,   30, 100]),
        'tcp'     => ist_la_rate($segments['tcp']['avg']     ?? 999, [20,   50, 150]),
        'tls'     => ist_la_rate($segments['tls']['avg']     ?? 999, [30,   80, 200]),
    ];

    $wallEnd    = function_exists('hrtime') ? hrtime(true) : (int)(microtime(true)*1e9);
    $durationMs = round(($wallEnd - $wallStart)/1e6, 1);

    /* ── Assemble result ────────────────────────────────────────────────── */
    $result = [
        'ok'          => true,
        'url'         => $rawUrl,
        'host'        => $host,
        'count'       => $count,
        'method'      => $method,
        'duration_ms' => $durationMs,
        'meta'        => $meta ?? [],
        'segments'    => $segments,
        'stats'       => $stats,
        'ratings'     => $ratings,
        'probes'      => $probes,
        'timestamp'   => time(),
    ];

    /* Store in SQLite and attach history */
    $history        = ist_la_history($rawUrl, 9);
    $result['saved_id'] = ist_la_save($rawUrl, $result);
    $result['history']  = $history;

    echo json_encode($result);
    exit;
}

