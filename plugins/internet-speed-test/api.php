/* ─── ANSI helpers ───────────────────────────────────────────────────────── */
define('IST_A0',   "\033[0m");
define('IST_AB',   "\033[1m");
define('IST_AD',   "\033[2m");
define('IST_ACY',  "\033[36m");
define('IST_ABCY', "\033[96m");
define('IST_AGR',  "\033[32m");
define('IST_ABGR', "\033[92m");
define('IST_ARE',  "\033[31m");
define('IST_ABRE', "\033[91m");
define('IST_AYE',  "\033[33m");
define('IST_ABYE', "\033[93m");
define('IST_AMA',  "\033[35m");
define('IST_ABMA', "\033[95m");
define('IST_AGY',  "\033[90m");
define('IST_AWH',  "\033[97m");
define('IST_ABBL', "\033[94m");
define('IST_W',    66);

function ist_apad(string $s, int $w): string {
    $plain = preg_replace('/\033\[[0-9;]*m/', '', $s);
    return $s . str_repeat(' ', max(0, $w - mb_strlen($plain)));
}
function ist_row(string $label, string $value): string {
    return '  ' . IST_ABYE . IST_AB . str_pad($label, 22) . IST_A0 . IST_AWH . $value . IST_A0 . "\n";
}
function ist_sec(string $title): string {
    $ruleLen = max(4, IST_W - mb_strlen($title) - 3);
    return "\n" . IST_ABCY . IST_AB . '◆' . IST_A0 . ' ' . IST_AWH . IST_AB . $title . IST_A0
         . '  ' . IST_AGY . str_repeat('─', $ruleLen) . IST_A0 . "\n\n";
}
function ist_bar(float $val, float $max, int $width = 20): string {
    $filled = $max > 0 ? (int)min($width, round($val / $max * $width)) : 0;
    return str_repeat('█', $filled) . IST_AGY . str_repeat('░', $width - $filled) . IST_A0;
}
function ist_grade_clr(string $g): string {
    return match($g) { 'A+', 'A' => IST_ABGR, 'B' => IST_ABYE, 'C' => IST_AYE, default => IST_ABRE };
}

