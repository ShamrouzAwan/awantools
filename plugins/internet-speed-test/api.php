/* ─── HTTP fetch helper ──────────────────────────────────────────────────── */
function ist_fetch(string $url, array $opts = []): array {
    $timeout = (int)($opts['timeout'] ?? 10);
    $method  = strtoupper($opts['method'] ?? 'HEAD');

    if (function_exists('curl_init')) {
        $ch = curl_init();
        curl_setopt_array($ch, [
            CURLOPT_URL            => $url,
            CURLOPT_RETURNTRANSFER => true,
            CURLOPT_TIMEOUT        => $timeout,
            CURLOPT_FOLLOWLOCATION => false,
            CURLOPT_CUSTOMREQUEST  => $method,
            CURLOPT_HEADER         => true,
            CURLOPT_SSL_VERIFYPEER => false,
            CURLOPT_SSL_VERIFYHOST => 0,
            CURLOPT_USERAGENT      => 'Mozilla/5.0 (compatible; AwanTools/1.0; +https://awantools.site)',
            CURLOPT_NOBODY         => ($method === 'HEAD'),
        ]);
        $raw   = curl_exec($ch);
        $info  = curl_getinfo($ch);
        $errno = curl_errno($ch);
        curl_close($ch);
        if ($errno) return ['error' => curl_error($ch), 'code' => 0, 'time' => 0];
        return ['body' => substr($raw, $info['header_size']), 'code' => (int)$info['http_code'], 'time' => (float)$info['total_time'] * 1000, 'error' => null];
    }

    $ctx  = stream_context_create(['http' => ['method' => $method, 'timeout' => $timeout, 'ignore_errors' => true], 'ssl' => ['verify_peer' => false]]);
    $t0   = microtime(true);
    @file_get_contents($url, false, $ctx);
    $t1   = microtime(true);
    return ['code' => 0, 'time' => ($t1 - $t0) * 1000, 'error' => null];
}

